public class Calculator {

    public float plus(float num_1, float num_2){
        return num_1 + num_2;
    }

    public float minus(float num_1, float num_2){
        return num_1 - num_2;
    }

    public float multi(float num_1, float num_2){
        return num_1 * num_2;
    }

    public float div(float num_1, float num_2){
        return num_1 / num_2;
    }
}
